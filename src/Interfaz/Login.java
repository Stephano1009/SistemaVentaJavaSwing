    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String usuario = txtUsuario.getText();
        char[] password = pswPassword.getPassword();
        String contraString = new String(password);
        if (!usuario.isEmpty() && password.length != 0) {
            Usuario usu = dao.login(usuario, contraString);
            if (usu != null) {
                JOptionPane.showMessageDialog(null, "Inicio de sesión exitoso");
                Plataforma plataforma = new Plataforma();
                plataforma.getNombreusuario(usu.getNombreCargo(),usu.getNombreEmpleado());
                plataforma.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usuario o Contraseña incorrecta");
                this.limpiarCajas();
            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

    public void limpiarCajas() {
        txtUsuario.setText("");
        pswPassword.setText("");
        txtUsuario.requestFocus();
    }
    
